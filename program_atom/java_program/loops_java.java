public class loops_java
{
	public static void main(String args[])
	{
		for(int i=0;i<6;i++)
		{
			System.out.println("Hello");					
		}
		int t=5;
		while(t>0)
		{
			System.out.println("Hello World");
			t--;
		}
		t=6;
		do
		{
			System.out.println("Have a nice day!");
			t--;
		}
		while(t>0);
	}
}